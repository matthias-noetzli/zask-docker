    <h1>Theoretischer Hintergrund</h1>
    <p>
      Der Früherkennung des Autismus hat eine zentrale Bedeutung, deren
      Umsetzung in verschiedenen regionalen und nationalen Autismus-Strategien
      in der Schweiz und Deutschland als wesentlich und zugleich
      entwicklungsbedürftig benannt wird (Schweizerische Eidgenossenschaft,
      2018; Witzmann, Dose & Noterdaeme, 2023).
    </p>

    <p>
      In der aktuellen Praxis der Früherkennung werden neben verschiedenen
      Checklisten zu den «red flags» im frühen Kindesalter bewährte (z. B. die
      «Checklist of 16 Early Signs of Autism by 16 Months»), zugleich ältere
      Screening-Instrumente (z. B. M-CHAT, BSFA) eingesetzt (Lütolf, 2022).
      Diese Instrumente haben jedoch primär die Kinder im Blick, die bereits im
      zweiten und dritten Lebensjahr deutlich sichtbare autismusspezifische
      Auffälligkeiten zeigen. Es fehlen Screening-Instrumente, die zum einen die
      Lücke zu den ab dem fünften Lebensjahr oder später ansetzenden Screenings
      wie dem FSK, der SRS oder dem MBAS schließen (Hofer & Fellinger, 2022),
      zum anderen bereits im jungen Kindesalter das breite Autismus-Spektrum mit
      seinen heterogenen Erscheinungsformen und Entwicklungsverläufen abzudecken
      versuchen. Hier setzt das Zürcher Autismus Screening für das Kindesalter
      (ZASK)
    </p>

    <p>
      Der ZASK dient (heil)pädagogischen und therapeutischen Fachpersonen zur
      kriteriengeleiteten Überprüfung, Reflexion und Kommunikation von
      Besonderheiten aus dem Autismus-Spektrum. Auf der Basis von Beobachtungen
      in unterschiedlichen Situationen, z.B. strukturiert – unstrukturiert;
      Alltag – Spielen – Lernen – Interaktion, wird der Blick auf
      autismusspezifische Besonderheiten geworfen. Ergänzend können
      Informationen aus Gesprächen mit den Bezugspersonen und/oder dem Kind
      einbezogen werden. Das Screening orientiert sich an den Diagnosekriterien
      der ICD-11.
    </p>

    <p>
      Aufgrund des Wissens um die zunehmend stabile Prävalenz diagnostisch
      relevanter Symptome des Autismus nach dem zweiten Lebensjahr zielt der
      ZASK auf eine Anwendung ab dem dritten Lebensjahr und kann bis zum Ende
      des zwölften Lebensjahres genutzt werden.
    </p>

    <p>
      Mit dem ZASK können folgende, für die Früherkennung von Autismus zentrale
      Entwicklungsbereiche gezielt beobachtet werden:
    </p>
    <ul>
      <li>«Soziale Wahrnehmung und Soziale Kommunikation»</li>
      <li>«Interessen, Aktivitäten und Verhaltensmuster»</li>
      <li>«Kognitiver und verbalsprachlicher Entwicklungsstand»</li>
    </ul>

